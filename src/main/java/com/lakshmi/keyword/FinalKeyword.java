package com.lakshmi.keyword;
//final keyword is used to keep the value constant,its value can't be modified
//final keyword can apply to varaibales,methods,classes
//final classes can't be inhertied ,final method can't be overriden,variables costant
public class FinalKeyword {
	public static void main(String[] args) {
	final int x=20;
	//x=x+10;we can't change the value of x
	System.out.println(x);
	}
}
